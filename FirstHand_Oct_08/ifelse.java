		 System.out.println(1000 >= 1000); // true
		 System.out.println(100 <= 1000); // true
		 System.out.println(100 <= 100); // true
		 
		 System.out.println(200 <= 100); // false
		 
		 
		 System.out.println(100 == 200);//false

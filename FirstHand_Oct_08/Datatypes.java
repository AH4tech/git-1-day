package PrecourseBeta;

public class Datatypes {
	
	public static void main(String args[]) {
		
		byte my_age = 29;
		short my_salary = 10000;
		int pincode = 122001;
		long my_phone = 9863726736388L;
		
		float percentage = 91.498988f;
		double annualsalary = 27378263.37487438743d;
		
		// 2.7378263374874387E7 == 27378263.374874387 * 10^7
		
		boolean isIndian = true;
		char section = 'B';
		
		String my_name = "Vikas Ranjan 2378237 #%^@%#^#@^&";
		
		// double quotes, String datatype, it can have anything at all
		
		
		System.out.println(my_age);
		System.out.println(my_salary);
		System.out.println(pincode);
		System.out.println(my_phone);
		System.out.println(percentage);
		System.out.println(annualsalary);
		System.out.println(isIndian);
		System.out.println(section);
		System.out.println(my_name);
		
		
		short old_age = 29;// old_age stores "short" datatype
		int new_age = old_age;// new_age is "int" datatype which is bigger
		
		
	}
}


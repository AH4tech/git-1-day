package Practise_Oct_27;

public class doublefloat {
	

	public static void main (String args[]) {
		
		int i = 10;
		
		float j = i;
		System.out.println(j);//10.0
		
		
		double k = i;
		System.out.println(k);//10.0
		
		
		double m = 10;
		System.out.println(m);
		float p = 10;
		System.out.println(p);
	}


}

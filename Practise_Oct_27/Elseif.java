public class Elseif {

	public static void main (String args[]) {
		
		
		int num = 0;
		
		if(num > 0) {
			System.out.print("Positive number");
		}else if (num < 0) {
			System.out.print("Negative number");
		}else {
			System.out.print("Number is zero");
		}
		
		
	}
}


package PrecourseGama_FirstHand_Oct_17;

public class ifelse {
	public static void main(String args[]) {
		
		int num1 = 100;
		int num2 = 10;
		
		if (num1 == num2) { //false
			System.out.println("Equal");
		}else {
			System.out.println("NOt Equal");
		}
		
		
		System.out.println(10 == 100);//false
		System.out.println(100 == 100);//true
		System.out.println(100 > 5);//true
		System.out.println(10 > 1); //true
		System.out.println(10 > 100); //false
		System.out.println(10 < 100); //true
		System.out.println(100 < 100); //false
		System.out.println(100 <= 100); //true
		System.out.println(1000 <= 100); //false
		System.out.println(100 >= 100); //true
		System.out.println(1000 >= 100); //true
		System.out.println(1 >= 100); //false
	}
}

